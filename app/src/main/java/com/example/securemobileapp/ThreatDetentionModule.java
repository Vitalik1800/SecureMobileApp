    public List<String> checkForThreats(Context context) {
        List<String> detectedThreats = new ArrayList<>();
        PackageManager packageManager = context.getPackageManager();
        @SuppressLint("QueryPermissionsNeeded") List<PackageInfo> installedPackages = packageManager.getInstalledPackages(0);

        for (PackageInfo packageInfo : installedPackages) {
            if (THREAT_LIST.contains(packageInfo.packageName)) {
                detectedThreats.add(packageInfo.packageName);
            }
        }

        return detectedThreats;
    }


